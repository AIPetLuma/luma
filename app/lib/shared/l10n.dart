  final Locale locale;

  L10n(this.locale);

  static L10n of(BuildContext context) {
    return Localizations.of<L10n>(context, L10n) ?? L10n(const Locale('en'));
  }

  static const supportedLocales = [Locale('en'), Locale('zh')];

  bool get _isZh => locale.languageCode == 'zh';

  // ── App ──
