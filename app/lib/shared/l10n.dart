  String get bringToLife => _isZh ? '赋予生命' : 'Bring to life';

  // ── Home ──
  String day(int n) => _isZh ? '第 $n 天' : 'Day $n';
  String get talk => _isZh ? '聊天' : 'Talk';
  String diary(int count) => _isZh ? '日记 ($count)' : 'Diary ($count)';

  // ── Home: Emotion labels ──
