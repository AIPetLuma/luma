      ? '你的对话由 AI 系统处理。Luma 有模拟的情绪和记忆 — 感觉很真实，但都是软件生成的。'
      : 'Your conversations are processed by an AI system. '
          'Luma has simulated emotions and memory — they feel '
          'real, but they are generated by software.';
